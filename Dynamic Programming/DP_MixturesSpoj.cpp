ll mix(ll i , ll j)
{
    if(i==j) return 0 ;
    if(dp[i][j]!=-1) return dp[i][j];

    dp[i][j]=INT_MAX ;

    for(ll k=i ; k<=j ; k++)
    {
        dp[i][j]=min(dp[i][j] , mix(i,k)+mix(k+1,j)+csum(i,k)*csum(k+1,j));
    }
    return dp[i][j];
}

